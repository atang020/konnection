    <h2>District</h2>
    <p>Each division then falls under a district which is usually bounded by
      state lines. During the annual district convention, delegates from all clubs
      within a district elect a Governor Secretary and Treasurer to serve on the
      District Executive Board. A board of district chairs are then appointed to
      carry out specific district focuses and events throughout the year in which
      clubs from all over the district can participate and attend. UCSD Circle K
      is part of the largest district in CKI, the Cal-Nev-Ha district, which
      comprises California, Nevada, and Hawaii. Our district spans over 2700
      miles and holds 40 clubs with over hundreds of members.</p>

